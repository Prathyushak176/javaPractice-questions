package Practice;

public class Factorial {
	public static void main(String[] args) {
		//question 9(factorial using for loop)
				int b=5;
				int fact =1;
				
				for(int m=1;m<=b;m++) {
					fact = fact*m;
				}
					System.out.println("factorial of"+ b +":" +fact);
					
					 
					
				
	}

}
